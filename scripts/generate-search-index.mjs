// Generates src/data/searchIndex.generated.js — a small index of the notable
// figure/place/item card names on the country & region pages, so Global Search
// can find e.g. "Stigis" or "Emperor Yddris" and jump to the page featuring it.
// Adventure card names are searched at runtime (the adventure data is already in
// memory), so only the markdown-frontmatter pages need indexing here.
//
// Re-run after editing country/region notable cards:  node scripts/generate-search-index.mjs
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import yaml from "js-yaml";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const locDir = path.join(root, "src/data/locations");
const compDir = path.join(root, "src/data/compendium");

// Map a compendium detail path → owning pin id (from locations/<id>.js).
const detailToPin = {};
for (const file of fs.readdirSync(locDir)) {
  if (!file.endsWith(".js")) continue;
  const src = fs.readFileSync(path.join(locDir, file), "utf8");
  const m = src.match(/detail:\s*"([^"]+)"/);
  if (m) detailToPin[m[1].replace(/\\/g, "/")] = file.replace(/\.js$/, "");
}

const walk = (dir) =>
  fs.readdirSync(dir, { withFileTypes: true }).flatMap((e) => {
    const p = path.join(dir, e.name);
    return e.isDirectory() ? walk(p) : p.endsWith(".md") ? [p] : [];
  });

const mentions = [];
const seen = new Set();
for (const file of walk(path.join(compDir, "Geography"))) {
  const md = fs.readFileSync(file, "utf8");
  if (!md.startsWith("---")) continue;
  const fm = md.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?([\s\S]*)$/);
  if (!fm) continue;
  let meta;
  try { meta = yaml.load(fm[1]) ?? {}; } catch { continue; }
  const rel = path.relative(compDir, file).replace(/\\/g, "/");
  const pinId = detailToPin[rel];
  if (!pinId) continue;
  const title = (fm[2].match(/^#\s+(.+)$/m)?.[1] ?? pinId).trim();
  for (const group of ["figures", "places", "items"]) {
    for (const card of meta[group] ?? []) {
      if (!card?.name) continue;
      const key = `${card.name}::${pinId}`;
      if (seen.has(key)) continue;
      seen.add(key);
      mentions.push({ name: card.name, pinId, page: title });
    }
  }
}

mentions.sort((a, b) => a.name.localeCompare(b.name));
const out =
  "// AUTO-GENERATED by scripts/generate-search-index.mjs — do not edit by hand.\n" +
  "// Notable card names on country/region pages, for Global Search.\n" +
  `export const mentionIndex = ${JSON.stringify(mentions, null, 2)};\n`;
fs.writeFileSync(path.join(root, "src/data/searchIndex.generated.js"), out);
console.log(`Wrote ${mentions.length} mentions to src/data/searchIndex.generated.js`);
