// Generates src/data/entryBlurbs.generated.js — a map of compendium page slug →
// a short one-line blurb (the first prose sentence of the page). Hub / browse
// cards show this under the title so the gallery has context, without eagerly
// importing every lazy-loaded markdown file.
//
// Re-run after editing page intros:  node scripts/generate-entry-blurbs.mjs
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const compDir = path.join(root, "src/data/compendium");
const MAX = 160;

const walk = (dir) =>
  fs.readdirSync(dir, { withFileTypes: true }).flatMap((e) => {
    const p = path.join(dir, e.name);
    return e.isDirectory() ? walk(p) : p.endsWith(".md") ? [p] : [];
  });

// First prose sentence of a page: drop frontmatter, images, headings and list
// markers, take the first non-empty paragraph, then its first sentence.
function blurb(md) {
  let body = md.replace(/^---\r?\n[\s\S]*?\r?\n---\r?\n?/, "");      // frontmatter
  body = body.replace(/!\[[^\]]*\]\([^)]*\)/g, "");                  // images
  const para = body
    .split(/\r?\n\s*\r?\n/)
    .map((p) => p.replace(/^#+\s.*$/gm, "").replace(/^[>*-]\s+/gm, "").replace(/\s+/g, " ").trim())
    .find((p) => p.length > 0);
  if (!para) return null;
  // First sentence (up to ". "), else the paragraph; clamp with an ellipsis.
  const m = para.match(/^(.*?[.!?])(?:\s|$)/);
  let text = m ? m[1] : para;
  if (text.length > MAX) text = text.slice(0, MAX - 1).replace(/\s+\S*$/, "") + "…";
  return text;
}

const map = {};
for (const file of walk(compDir).sort()) {
  const slug = path.basename(file, ".md");
  if (slug in map) continue; // first match wins, stable result
  const b = blurb(fs.readFileSync(file, "utf8"));
  if (b) map[slug] = b;
}

const out =
  "// AUTO-GENERATED by scripts/generate-entry-blurbs.mjs — do not edit by hand.\n" +
  "// Compendium page slug → first-sentence blurb, shown on hub / browse cards.\n" +
  "export const entryBlurbs = " +
  JSON.stringify(map, null, 2) +
  ";\n";

fs.writeFileSync(path.join(compDir, "..", "entryBlurbs.generated.js"), out);
console.log(`Wrote entryBlurbs.generated.js with ${Object.keys(map).length} blurbs.`);
