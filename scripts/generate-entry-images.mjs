// Generates src/data/entryImages.generated.js — a small map of compendium page
// slug → its first embedded image. This lets a card with a "More info" link but
// no image of its own borrow the linked page's image (e.g. a card that links to
// the Orc page shows the generic orc picture).
//
// Keeping this as a generated map (rather than eager-importing every markdown
// file) keeps the lazy-loaded compendium markdown out of the main bundle.
//
// Re-run after adding/removing page images:  node scripts/generate-entry-images.mjs
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import sharp from "sharp";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const compDir = path.join(root, "src/data/compendium");
const publicDir = path.join(root, "public");

// Same image-matcher Compendium.jsx uses: ![alt](url "optional title").
const IMAGE_RE = /!\[([^\]]*)\]\(([^")]+?)(?:\s+"([^"]*)")?\)/;
const IMAGE_RE_G = new RegExp(IMAGE_RE, "g");

const walk = (dir) =>
  fs.readdirSync(dir, { withFileTypes: true }).flatMap((e) => {
    const p = path.join(dir, e.name);
    return e.isDirectory() ? walk(p) : p.endsWith(".md") ? [p] : [];
  });

const map = {};       // slug → first image (stable, for the "borrow" path)
const allMap = {};    // slug → every image on the page (for randomized browse cards)
for (const file of walk(compDir).sort()) {
  const slug = path.basename(file, ".md");
  if (slug in map) continue; // first file wins, for a stable result
  const body = fs.readFileSync(file, "utf8");
  const srcs = [];
  for (const m of body.matchAll(IMAGE_RE_G)) {
    const src = m[2].trim();
    if (!srcs.includes(src)) srcs.push(src); // dedupe, keep page order
  }
  // Adventures keep their art in YAML frontmatter `image:` fields (on their NPC /
  // place / item cards) rather than in markdown image syntax, so pull those in
  // too - otherwise the Adventures browse hub has nothing to borrow.
  if (file.includes(`${path.sep}Adventures${path.sep}`)) {
    // The path may contain apostrophes (e.g. "Ma'tho Fherengi's Vault"), so only
    // surrounding quotes are stripped - never quote characters inside the value.
    for (const m of body.matchAll(/^\s*image:\s*["']?(\/[^\n]+?)["']?\s*$/gm)) {
      const src = m[1].trim();
      if (!srcs.includes(src)) srcs.push(src);
    }
  }
  if (srcs.length) {
    map[slug] = srcs[0];
    allMap[slug] = srcs;
  }
}

// Orientation of every image, so browse cards can frame each shape without an
// ugly crop: portrait art (the bulk) gets a tall frame, roughly-square art a
// square frame, and the rest the default wide frame. A ~1:1 image is "square"
// (within 20% of equal sides) so it is not stretched into the widescreen card.
// Art much taller than 2:3 (ratio < 0.6, in practice the 9:16 renders) is "tall"
// so its head and feet are not cropped away by the 2:3 portrait frame.
// Cached by src so a shared image is only probed once.
const isPortrait = {};
const isSquare = {};
const isTall = {};
for (const src of [...new Set(Object.values(allMap).flat())]) {
  try {
    const meta = await sharp(path.join(publicDir, decodeURIComponent(src))).metadata();
    const { width: w, height: h } = meta;
    const r = w && h ? w / h : 0;
    isSquare[src] = r > 0 && r > 0.83 && r < 1.2;
    isTall[src] = r > 0 && r < 0.6;
    isPortrait[src] = !!(h && w && h > w) && !isSquare[src] && !isTall[src];
  } catch { isPortrait[src] = false; isSquare[src] = false; isTall[src] = false; /* missing/unreadable image — leave unmarked */ }
}
// Per-image orientation, keyed by src, so a card frames itself to the real shape
// of the image it shows (portrait art in a portrait frame, wide art in a wide
// one) instead of a fixed per-type default. Landscape is the default, so only
// tall/portrait/square are stored.
const imageOrientation = {};
for (const src of Object.keys(isPortrait)) {
  if (isSquare[src]) imageOrientation[src] = "square";
  else if (isTall[src]) imageOrientation[src] = "tall";
  else if (isPortrait[src]) imageOrientation[src] = "portrait";
}

// First-image taller-than-wide set, kept for the stable "borrow" path (tall
// images count too - the set only picks the coarse tall-vs-wide fallback frame).
const portrait = Object.entries(map)
  .filter(([, src]) => isPortrait[src] || isTall[src])
  .map(([slug]) => slug)
  .sort();
// Per-image variant for randomized browse: slug → [{ src, portrait, square, tall }, ...].
const allImages = {};
for (const [slug, srcs] of Object.entries(allMap)) {
  allImages[slug] = srcs.map((src) => ({ src, portrait: isPortrait[src], square: isSquare[src], tall: isTall[src] }));
}

fs.writeFileSync(
  path.join(compDir, "..", "entryImages.generated.js"),
  "// AUTO-GENERATED by scripts/generate-entry-images.mjs — do not edit by hand.\n" +
  "// Compendium page slug → first embedded image, so a linked card can borrow it.\n" +
  "export const entryImages = " + JSON.stringify(map, null, 2) + ";\n"
);
fs.writeFileSync(
  path.join(compDir, "..", "entryImagePortrait.generated.js"),
  "// AUTO-GENERATED by scripts/generate-entry-images.mjs — do not edit by hand.\n" +
  "// Slugs whose first image is portrait (taller than wide), for card framing.\n" +
  "export const portraitSlugs = new Set(" + JSON.stringify(portrait, null, 2) + ");\n"
);
fs.writeFileSync(
  path.join(compDir, "..", "entryImagesAll.generated.js"),
  "// AUTO-GENERATED by scripts/generate-entry-images.mjs — do not edit by hand.\n" +
  "// Compendium page slug → every embedded image [{ src, portrait, square, tall }],\n" +
  "// so browse cards can show a random one instead of always the first.\n" +
  "export const entryImagesAll = " + JSON.stringify(allImages, null, 2) + ";\n"
);
fs.writeFileSync(
  path.join(compDir, "..", "imageOrientation.generated.js"),
  "// AUTO-GENERATED by scripts/generate-entry-images.mjs — do not edit by hand.\n" +
  "// Image src → \"tall\" | \"portrait\" | \"square\" (landscape omitted), so a card\n" +
  "// frames itself to the real shape of the image it shows.\n" +
  "export const imageOrientation = " + JSON.stringify(imageOrientation, null, 2) + ";\n"
);
const totalImgs = Object.values(allImages).reduce((n, a) => n + a.length, 0);
console.log(`Wrote entryImages.generated.js (${Object.keys(map).length} pages) + entryImagePortrait.generated.js (${portrait.length} portrait) + entryImagesAll.generated.js (${totalImgs} images) + imageOrientation.generated.js (${Object.keys(imageOrientation).length} oriented).`);
