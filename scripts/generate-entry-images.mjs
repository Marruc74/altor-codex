// Generates src/data/entryImages.generated.js — a small map of compendium page
// slug → its first embedded image. This lets a card with a "More info" link but
// no image of its own borrow the linked page's image (e.g. a card that links to
// the Orc page shows the generic orc picture).
//
// Keeping this as a generated map (rather than eager-importing every markdown
// file) keeps the lazy-loaded compendium markdown out of the main bundle.
//
// Re-run after adding/removing page images:  node scripts/generate-entry-images.mjs
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const compDir = path.join(root, "src/data/compendium");

// Same image-matcher Compendium.jsx uses: ![alt](url "optional title").
const IMAGE_RE = /!\[([^\]]*)\]\(([^")]+?)(?:\s+"([^"]*)")?\)/;

const walk = (dir) =>
  fs.readdirSync(dir, { withFileTypes: true }).flatMap((e) => {
    const p = path.join(dir, e.name);
    return e.isDirectory() ? walk(p) : p.endsWith(".md") ? [p] : [];
  });

const map = {};
for (const file of walk(compDir).sort()) {
  const slug = path.basename(file, ".md");
  if (slug in map) continue; // first match wins, for a stable result
  const m = fs.readFileSync(file, "utf8").match(IMAGE_RE);
  if (m) map[slug] = m[2].trim();
}

const out =
  "// AUTO-GENERATED by scripts/generate-entry-images.mjs — do not edit by hand.\n" +
  "// Compendium page slug → first embedded image, so a linked card can borrow it.\n" +
  "export const entryImages = " +
  JSON.stringify(map, null, 2) +
  ";\n";

fs.writeFileSync(path.join(compDir, "..", "entryImages.generated.js"), out);
console.log(`Wrote entryImages.generated.js with ${Object.keys(map).length} page images.`);
