// Generate the compendium cross-reference index: which pages mention which other
// pages in their prose. Powers "Referenced by" backlinks, auto-suggested related,
// and place->entry links. Run after editing page bodies:
//
//   node scripts/generate-crossrefs.mjs   (or npm run crossrefs)
//
// Output: src/data/crossRefs.generated.js
//   { mentions: { slug: [slug…] },  backlinks: { slug: [slug…] } }
// The audit fails if this file is stale.

import { readFileSync, readdirSync, statSync, writeFileSync, existsSync } from "fs";
import path from "path";

const ROOT = "src/data/compendium";
const OUT = "src/data/crossRefs.generated.js";
const toSlug = (s) => s.normalize("NFD").replace(/[̀-ͯ]/g, "").toLowerCase().replace(/['‘’ʼ]/g, "").replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");

// Generic single-word page names that would over-match in prose (mundane animals,
// elements, materials). Excluded as link TARGETS so cross-refs stay meaningful.
const STOP = new Set([
  "bison", "wolverine", "beaver", "barracuda", "narwhal", "piranha", "stingray", "aurochs",
  "moose", "eagle", "llama", "reindeer", "pig", "yak", "walrus", "shark", "dolphin", "scorpion",
  "spider", "hyena", "fox", "wolf", "lion", "tiger", "leopard", "jaguar", "cheetah", "mammoth",
  "elephant", "crocodile", "alligator", "bear", "boar", "gorilla", "yeti", "cobra", "snake",
  "metals", "galvorn", "climate", "trade", "heroes", "divination", "languages", "familiars",
]);

// Collect target pages (name -> slug) and documents to scan (slug + body).
const targets = new Map(); // slug -> name
const docs = []; // { slug, body }
(function walk(d) {
  for (const n of readdirSync(d)) {
    const f = path.join(d, n);
    if (statSync(f).isDirectory()) { if (path.basename(f) === "Adventures") continue; walk(f); continue; }
    if (!n.endsWith(".md")) continue;
    const slug = path.basename(f, ".md");
    if (slug.startsWith("_")) continue;
    let raw = readFileSync(f, "utf8").replace(/^---[\s\S]*?\n---\n/, "");
    const name = (raw.match(/^#\s+(.+)$/m)?.[1] ?? slug).trim();
    const body = raw.replace(/!\[[^\]]*\]\([^)]*\)/g, "").replace(/^#.*$/gm, "").trim();
    targets.set(slug, name);
    docs.push({ slug, body });
  }
})(ROOT);

// also add map pins as targets (some places have no md page of their own)
if (existsSync("src/data/locations.js")) {
  const loc = readFileSync("src/data/locations.js", "utf8");
  for (const m of loc.matchAll(/\bname:\s*"([^"]+)"/g)) {
    const slug = toSlug(m[1]);
    if (slug && !targets.has(slug)) targets.set(slug, m[1]);
  }
}

// Build one alternation regex of all matchable target names, longest first so the
// most specific name wins (e.g. "Fire Dragon" before "Dragon").
const esc = (s) => s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
const nameToSlug = new Map();
const pats = [];
for (const [slug, name] of targets) {
  const oneWord = !/\s/.test(name);
  if (oneWord && (name.length < 5 || STOP.has(slug))) continue; // skip noisy short/generic single words
  nameToSlug.set(name.toLowerCase(), slug);
  pats.push(name);
}
pats.sort((a, b) => b.length - a.length);
const re = new RegExp(`\\b(${pats.map(esc).join("|")})\\b`, "gi");

const mentions = {};
for (const { slug, body } of docs) {
  const hits = new Set();
  for (const m of body.matchAll(re)) {
    const t = nameToSlug.get(m[1].toLowerCase());
    if (t && t !== slug) hits.add(t);
  }
  if (hits.size) mentions[slug] = [...hits].sort();
}
// invert -> backlinks
const backlinks = {};
for (const [from, tos] of Object.entries(mentions)) {
  for (const to of tos) (backlinks[to] ??= []).push(from);
}
for (const k of Object.keys(backlinks)) backlinks[k].sort();

const sortObj = (o) => Object.fromEntries(Object.keys(o).sort().map((k) => [k, o[k]]));
const out = `// AUTO-GENERATED by scripts/generate-crossrefs.mjs - do not edit by hand.\n// Which compendium pages mention which (by prose), and the inverse (backlinks).\nexport const crossRefs = ${JSON.stringify({ mentions: sortObj(mentions), backlinks: sortObj(backlinks) }, null, 0)};\n`;
writeFileSync(OUT, out);
const edges = Object.values(mentions).reduce((a, v) => a + v.length, 0);
console.log(`Wrote ${OUT}: ${Object.keys(mentions).length} pages with mentions, ${edges} edges, ${pats.length} matchable targets.`);
