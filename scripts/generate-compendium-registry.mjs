// Generate the registry of markdown-only compendium pages, so videoData.js no
// longer needs hand-maintained EXTRA_* arrays. Scans the content sections and
// records each page's { section, group, name } (name taken from the first
// heading). videoData.js surfaces any page not already covered by a real
// chronicle video, deriving its .md path from the name.
//
//   node scripts/generate-compendium-registry.mjs
//
// Run after adding/removing/renaming pages in these sections. The audit
// (scripts/audit-compendium.mjs) fails if this file is out of date.

import { readFileSync, readdirSync, statSync, writeFileSync } from "fs";
import path from "path";

const toSlug = (s) => s.normalize("NFD").replace(/[̀-ͯ]/g, "").toLowerCase().replace(/['‘’ʼ]/g, "").replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
const ROOT = "src/data/compendium";
const OUT = "src/data/compendiumRegistry.generated.js";
// folder -> section key. Only these sections are filesystem-registered; Geography,
// History, Conflicts and Adventures are driven by videos / pins / their own glob.
const FOLDER_SECTION = { Creatures: "creatures", Peoples: "peoples", Lore: "lore", Magic: "magic", Characters: "characters" };

const rows = [];
for (const [folder, section] of Object.entries(FOLDER_SECTION)) {
  const base = path.join(ROOT, folder);
  (function walk(dir) {
    for (const n of readdirSync(dir)) {
      const f = path.join(dir, n);
      if (statSync(f).isDirectory()) { walk(f); continue; }
      if (!n.endsWith(".md")) continue;
      const slug = path.basename(f, ".md");
      if (slug.startsWith("_")) continue; // guides etc., not pages
      const rel = path.relative(base, f).split(path.sep);
      const group = rel.length === 2 ? rel[0] : null; // <Group>/<slug>.md vs <slug>.md
      const raw = readFileSync(f, "utf8");
      const name = (raw.match(/^#\s+(.+)$/m)?.[1] ?? slug).trim();
      // A page may opt out of the nav with `hidden: true` in its frontmatter:
      // it stays fully linkable, searchable and cross-referenced, but has no
      // entry in the section/group tree (surfaced only through links).
      const fm = raw.match(/^---\r?\n([\s\S]*?)\r?\n---/);
      const hidden = !!(fm && /^\s*hidden:\s*true\s*$/m.test(fm[1]));
      // slug is the file basename and is the source of truth for routing/dedup.
      // If the heading slugifies to something else (e.g. file umbran.md titled
      // "Umbra"), warn: such a page must be video-backed, or its .md won't load.
      if (toSlug(name) !== slug) console.warn(`  note: ${section}/${slug}.md heading "${name}" slugs to "${toSlug(name)}" != "${slug}"`);
      rows.push({ section, group, name, slug, hidden });
    }
  })(base);
}

rows.sort((a, b) =>
  a.section.localeCompare(b.section) ||
  (a.group ?? "").localeCompare(b.group ?? "") ||
  a.slug.localeCompare(b.slug)
);

const body = rows.map((r) => `  { section: ${JSON.stringify(r.section)}, group: ${JSON.stringify(r.group)}, name: ${JSON.stringify(r.name)}, slug: ${JSON.stringify(r.slug)}${r.hidden ? ", hidden: true" : ""} },`).join("\n");
const out = `// AUTO-GENERATED by scripts/generate-compendium-registry.mjs — do not edit by hand.\n// Markdown-only compendium pages (name = first heading). Re-run the script after\n// adding, removing or renaming pages in the content sections.\nexport const compendiumRegistry = [\n${body}\n];\n`;
writeFileSync(OUT, out);
console.log(`Wrote ${OUT} with ${rows.length} pages.`);
